package Batchclass;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		//Java Arithmetic Operators - 22.05.2023
		int a=5;
		int b=6;
		int C = a+b;
		System.out.println(C);
		
		int C1 = a-b;
		System.out.println(C1);
		
		int C2 = a*b;
		System.out.println(C2);
		
		int C3 = a/b;
		System.out.println(C3);
		
		int C4 = b/a;
		System.out.println(C4);
		
		float C5 = b/a;
		System.out.println(C5);
		
		double C6 = b/a;
		System.out.println(C6);
		
		int C7 = b%a; //remainder divide hone ke baad pending rehta hai wo
		System.out.println(C7); //remainder means remaining value
		
		int x=28;
		int y=29;
		int z = x%y;
		System.out.println(z);
		
		int p=21;
		int q=29;
		int r = p/q;
		System.out.println(r);
		
		float a1=21;
		float b1=29;
		float c1 = a1/b1;
		System.out.println(c1);
		
		//++i - Preincrement  i++ - Postincrement
		int i = 10;
		i = 1+i;
		System.out.println(i);
		System.out.println(i++);
		System.out.println(i);
		System.out.println(++i);
		
	}

}
